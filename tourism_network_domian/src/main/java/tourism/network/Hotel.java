package tourism.network;

public class Hotel {

    private String hotelName;
    private String hotelAdd;
    private String hotelPhoneNum;
    private String hotelMinPrice;
    private String hotelDesc;

    public String getHotelName() {
        return hotelName;
    }

    public void setHotelName(String hotelName) {
        this.hotelName = hotelName;
    }

    public String getHotelAdd() {
        return hotelAdd;
    }

    public void setHotelAdd(String hotelAdd) {
        this.hotelAdd = hotelAdd;
    }

    public String getHotelPhoneNum() {
        return hotelPhoneNum;
    }

    public void setHotelPhoneNum(String hotelPhoneNum) {
        this.hotelPhoneNum = hotelPhoneNum;
    }

    public String getHotelMinPrice() {
        return hotelMinPrice;
    }

    public void setHotelMinPrice(String hotelMinPrice) {
        this.hotelMinPrice = hotelMinPrice;
    }

    public String getHotelDesc() {
        return hotelDesc;
    }

    public void setHotelDesc(String hotelDesc) {
        this.hotelDesc = hotelDesc;
    }
}
